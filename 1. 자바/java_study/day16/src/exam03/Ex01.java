package exam03;

import static java.lang.Math.*;

public class Ex01 {
    public static void main(String[] args) {
        System.out.println(abs(-10));
        System.out.println(sqrt(9)); //  3
        System.out.println(pow(2,3)); // 8
        System.out.println(ceil(3.2)); // 4
        System.out.println(floor(3.6)); // 3
        System.out.println(round(5.6)); // 6
    }
}
